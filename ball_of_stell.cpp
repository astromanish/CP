#include <bits/stdc++.h>

using namespace std;

int main()
{
    int t;
    cin >> t;
    int n;
    long long k;
    while (t--)
    {
        cin >> n >> k;
        int i, x, y;
        vector<pair<int, int>> v;
        for (i = 0; i < n; i++)
        {
            cin >> x >> y;
            v.push_back(make_pair(x, y));
        }
    }
    return 0;
}